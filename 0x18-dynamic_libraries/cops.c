#include "main.h"

/**
 * add - gets the sum
 * @a: first int
 * @b: second int
 * Return: the sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - gets the difference
 * @a: first int
 * @b: second int
 * Return: the difference
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - gets the product
 * @a: first int
 * @b: second int
 * Return: the product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two int
 * @a: first int
 * @b: second int
 * Return: the result
 */

int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - gets the modulus
 * @a: first int
 * @b: second int
 * Return: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
